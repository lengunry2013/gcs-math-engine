    @Override
    public SlotBonusResult computeBonusStart(SlotGameLogicBean gameLogicBean, int payback) {
        if (bonusRandom == null) {
            bonusRandom = new RandomWeightUntil(BONUS_WEIGHT);
        }
        SlotBonusResult result = new SlotBonusResult();
        int randomIndex = bonusRandom.getRandomResult();
        int[] bonusReward = REWARD[randomIndex];
        long totalPay = 0;
        for (int reward : bonusReward) {
            totalPay += reward;
        }
        int index = -1;
        if (totalPay < GROUP_MIN) {
            index = 0;
        } else if (totalPay < GROUP_MAX) {
            index = 1;
        } else {
            index = 2;
        }
        long payForPickIndex = totalPay;
        RandomWeightUntil randomWeightUntil = new RandomWeightUntil(REWARD_DOUBLE[index]);
        int doubleIndex = randomWeightUntil.getRandomResult();
        if (doubleIndex == 1) {
            totalPay *= 2;
        }
        totalPay *= gameLogicBean.getSumBetCredit();
        result.setTotalPay(totalPay);
        result.setPayForPickIndex(payForPickIndex);
        result.setBonusPlayStatus(100);
        return result;
    }

    @Override
    public SlotBonusResult computeBonusStart(SlotGameLogicBean gameLogicBean, int payback, InputInfo input, RecoverInfo recoverInfo) {
        return computeBonusStart(gameLogicBean, payback);
    }

    @Override
    public SlotBonusResult computeBonusPick(SlotGameLogicBean gameLogicBean, PlayerInputInfo playerInfo, SlotBonusResult bonus, RecoverInfo recoverInfo) {
        if (bonus != null) {
            bonus.setBonusPlayStatus(1000);
        }
        return bonus;
    }
